using System.Text.Json;
using CDArchive.Core.Models;

namespace CDArchive.Core.Tests.Canon;

/// <summary>
/// Regression tests for display-node types (SubpieceDisplayNode, PieceOriginalNode,
/// VersionDisplayNode), RoleEntry serialization, CanonPiece.TreeChildren, and
/// computed piece/composer properties.
/// </summary>
public class CanonModelDisplayTests
{
    // ── SubpieceDisplayNode ──────────────────────────────────────────────────

    [Fact]
    public void SubpieceDisplayNode_Default_ExpandableWhenPieceHasSubpieces()
    {
        var piece = new CanonPiece
        {
            Title = "Allegro",
            Subpieces = [new CanonPiece { Title = "Part A" }]
        };
        var node = new SubpieceDisplayNode(piece);
        Assert.True(node.HasChildren);
        Assert.NotNull(node.Children);
        Assert.Single(node.Children!);
    }

    [Fact]
    public void SubpieceDisplayNode_Default_NotExpandableWhenNoSubpieces()
    {
        var node = new SubpieceDisplayNode(new CanonPiece { Title = "Allegro" });
        Assert.False(node.HasChildren);
        Assert.Null(node.Children);
    }

    [Fact]
    public void SubpieceDisplayNode_IsExpandableFalse_SuppressesExpanderEvenWithSubpieces()
    {
        var piece = new CanonPiece
        {
            Title = "Allegro",
            Subpieces = [new CanonPiece { Title = "Part A" }]
        };
        var node = new SubpieceDisplayNode(piece, isExpandable: false);
        Assert.False(node.HasChildren);
        Assert.Null(node.Children);
    }

    [Fact]
    public void SubpieceDisplayNode_FilteredChildren_UsedInsteadOfAutoGenerated()
    {
        var piece = new CanonPiece
        {
            Title = "Allegro",
            Subpieces = [new CanonPiece { Title = "Auto-Part" }]
        };
        var overrideChild = new SubpieceDisplayNode(new CanonPiece { Title = "Override-Part" });
        var node = new SubpieceDisplayNode(piece, filteredChildren: [overrideChild]);

        Assert.True(node.HasChildren);
        var children = node.Children!;
        Assert.Single(children);
        Assert.Equal("Override-Part", children[0].Piece.Title);
    }

    [Fact]
    public void SubpieceDisplayNode_AutoChildren_WrapSubpiecesAsDisplayNodes()
    {
        var piece = new CanonPiece
        {
            Title = "Sonata",
            Subpieces =
            [
                new CanonPiece { Title = "Allegro" },
                new CanonPiece { Title = "Andante" },
            ]
        };
        var node = new SubpieceDisplayNode(piece);
        var children = node.Children!;
        Assert.Equal(2, children.Count);
        Assert.Equal("Allegro", children[0].Piece.Title);
        Assert.Equal("Andante", children[1].Piece.Title);
    }

