    // ── RoleEntry parsing ────────────────────────────────────────────────────

    [Fact]
    public void ParseRoles_StringItems_ParsedWithNameOnly()
    {
        // Regression: an earlier bug silently dropped string-format role references
        var json = JsonDocument.Parse("[\"Florestan\", \"Leonore\"]").RootElement;
        var roles = RoleEntry.ParseRoles(json);

        Assert.Equal(2, roles.Count);
        Assert.Equal("Florestan", roles[0].Name);
        Assert.Equal("Leonore", roles[1].Name);
        Assert.Null(roles[0].VoiceType);
        Assert.Null(roles[1].VoiceType);
    }

    [Fact]
    public void ParseRoles_ObjectItems_ParsedWithAllFields()
    {
        var json = JsonDocument.Parse(
            "[{\"name\":\"Florestan\",\"voice_type\":\"tenor\",\"description\":\"A prisoner\"}]")
            .RootElement;
        var roles = RoleEntry.ParseRoles(json);

        var role = Assert.Single(roles);
        Assert.Equal("Florestan", role.Name);
        Assert.Equal("tenor", role.VoiceType);
        Assert.Equal("A prisoner", role.Description);
    }

    [Fact]
    public void ParseRoles_MixedItems_BothFormatsParsed()
    {
        var json = JsonDocument.Parse(
            "[{\"name\":\"Florestan\",\"voice_type\":\"tenor\"}, \"Leonore\"]")
            .RootElement;
        var roles = RoleEntry.ParseRoles(json);

        Assert.Equal(2, roles.Count);
        Assert.Equal("tenor", roles[0].VoiceType);
        Assert.Null(roles[1].VoiceType);
    }

    [Fact]
    public void ParseRoles_NonArrayElement_ReturnsEmpty()
    {
        var json = JsonDocument.Parse("{}").RootElement;
        var roles = RoleEntry.ParseRoles(json);
        Assert.Empty(roles);
    }

    // ── RoleEntry serialisation ──────────────────────────────────────────────

    [Fact]
    public void SerializeRoles_NameOnlyEntries_EmittedAsPlainStrings()
    {
        // Regression: an earlier bug always emitted objects even for name-only entries
        var roles = new List<RoleEntry>
        {
            new() { Name = "Florestan" },
            new() { Name = "Leonore" },
        };
        var json = RoleEntry.SerializeRoles(roles)!.Value;
        var raw = json.GetRawText();

        // Should be plain JSON strings, not objects
        Assert.Contains("\"Florestan\"", raw);
        Assert.DoesNotContain("\"name\"", raw);
    }

    [Fact]
    public void SerializeRoles_FullEntries_EmittedAsObjects()
    {
        var roles = new List<RoleEntry>
        {
            new() { Name = "Florestan", VoiceType = "tenor", Description = "A prisoner" }
        };
        var json = RoleEntry.SerializeRoles(roles)!.Value;
        var raw = json.GetRawText();

        Assert.Contains("\"name\"", raw);
        Assert.Contains("\"voice_type\"", raw);
        Assert.Contains("\"description\"", raw);
    }

    [Fact]
    public void SerializeRoles_EmptyList_ReturnsNull()
    {
        Assert.Null(RoleEntry.SerializeRoles([]));
    }

    [Fact]
    public void SerializeRoles_RoundTrip_PreservesData()
    {
        var original = new List<RoleEntry>
        {
            new() { Name = "Florestan", VoiceType = "tenor" },
            new() { Name = "Leonore" },    // name-only → string format
        };
        var json = RoleEntry.SerializeRoles(original)!.Value;
        var reparsed = RoleEntry.ParseRoles(json);

        Assert.Equal(2, reparsed.Count);
        Assert.Equal("Florestan", reparsed[0].Name);
        Assert.Equal("tenor", reparsed[0].VoiceType);
        Assert.Equal("Leonore", reparsed[1].Name);
        Assert.Null(reparsed[1].VoiceType);
    }

