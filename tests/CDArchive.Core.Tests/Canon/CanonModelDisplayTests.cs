    // ── Catalog / key formatting ─────────────────────────────────────────────

    [Fact]
    public void Catalog_SingleOpusEntry_FormatsCorrectly()
    {
        var piece = new CanonPiece
        {
            CatalogInfo = [new CatalogInfo { Catalog = "Op.", CatalogNumber = "67" }]
        };
        Assert.Equal("Op. 67", piece.Catalog);
    }

    [Fact]
    public void Catalog_WithSubnumber_IncludesHashSubnumber()
    {
        var piece = new CanonPiece
        {
            CatalogInfo =
            [
                new CatalogInfo { Catalog = "Op.", CatalogNumber = "27", CatalogSubnumber = "2" }
            ]
        };
        Assert.Contains("Op. 27", piece.Catalog);
        Assert.Contains("#2", piece.Catalog);
    }

    [Fact]
    public void Catalog_MultipleEntries_FormatsAll()
    {
        var piece = new CanonPiece
        {
            CatalogInfo =
            [
                new CatalogInfo { Catalog = "Op.", CatalogNumber = "27" },
                new CatalogInfo { Catalog = "WoO", CatalogNumber = "50" },
            ]
        };
        // Catalog returns only the FIRST entry (used for tree column display)
        Assert.Equal("Op. 27", piece.Catalog);
    }

    [Fact]
    public void Catalog_NoCatalogInfo_ReturnsEmpty()
    {
        var piece = new CanonPiece { Title = "Song" };
        Assert.Equal("", piece.Catalog);
    }

    [Fact]
    public void Key_MajorKey_ReturnsTonalityAsIs()
    {
        // Major keys return the tonality unchanged (capitalised), no "major" suffix
        var piece = new CanonPiece { KeyTonality = "C", KeyMode = "major" };
        Assert.Equal("C", piece.Key);
    }

    [Fact]
    public void Key_MinorKey_ReturnsTonalityLowercased()
    {
        // Minor keys return the tonality in lower-case, no "minor" suffix
        var piece = new CanonPiece { KeyTonality = "C-sharp", KeyMode = "minor" };
        Assert.Equal("c-sharp", piece.Key);
    }

    [Fact]
    public void Key_NoKeySet_ReturnsEmpty()
    {
        var piece = new CanonPiece { Title = "Untitled" };
        Assert.Equal("", piece.Key);
    }

